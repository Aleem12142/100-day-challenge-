function compoundOperators() {
    var x = 4;
    console.log("Initialy: x =", x);
    x += 2;
    console.log("After Addition: x =", x);
    x -= 1;
    console.log("After Subtraction: x =", x);
    x *= 4;
    console.log("After Multiplication: x =", x);
    x /= 2;
    console.log("After Division: x =", x);
}
compoundOperators();
